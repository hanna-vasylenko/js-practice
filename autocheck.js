class StringBuilder {
  constructor(value) {
    this.value = value;
  }
  getValue() {
    return this.value;
  }
  padEnd(str) {
    this.value.push(str);
  }
  padStart(str) {
    this.value.splice(0, 0, str);
  }
  padBoth(str) {
    this.value.splice(0, 0, str).push(str);
  }
}
